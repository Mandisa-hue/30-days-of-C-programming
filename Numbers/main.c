#include <stdio.h>
#include <stdlib.h>

int main()
{
    /*
        If you use float yo get decimal points
        if you use double you get an intiger
        if you use character you get one letter
        if you use power for exponents
        you use square roots
        if you use ceil it's to round a number up
        if you use floor it is to round a number down

     */
    printf("%f\n", 7/9.5);
    printf("%c\n", 'i');
    printf("%f\n", pow(2, 4));
    printf("%f\n", sqrt(64));
    printf("%f\n", ceil(9.8876));
    printf("%f\n"), floor(7.45466);
    return 0;
}
